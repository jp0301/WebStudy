--○ 지금 열람하는 게시물의 이후 게시물
SELECT NVL(MAX(NUM), -1) AS NEXTNUM
FROM TBL_BOARD
WHERE NUM > 500;

--○ 지금 열람하는 게시물의 이전 게시물
SELECT NVL(MIN(NUM), -1) AS BEFORENUM
FROM TBL_BOARD
WHERE NUM < 500;

